// Use the asynchronous BGParse JSRT APIs in a synchronous call
int32_t RunBgParseSync(const char *fileContents, uint32_t lengthBytes, const char *fileName)
{
    JsValueRef scriptSource;
    JsErrorCode e = (ChakraRTInterface::JsCreateExternalArrayBuffer(
        const_cast<char *>(fileContents), lengthBytes, nullptr, const_cast<char *>(fileContents), &scriptSource));

    // What's the preferred way of doing this?
    char16_t fileNameWide[MAX_PATH] = {0};
    size_t fileNameLength = strlen(fileName);
    for (size_t i = 0; i < fileNameLength; i++)
    {
        fileNameWide[i] = fileName[i];
    }

    JsScriptContents scriptContents = {0};
    scriptContents.container = const_cast<char *>(fileContents);
    scriptContents.containerType = JsScriptContainerType::HeapAllocatedBuffer;
    scriptContents.encodingType = JsScriptEncodingType::Utf8;
    scriptContents.contentLengthInBytes = lengthBytes;
    scriptContents.fullPath = fileNameWide;

    uint32_t cookie = 0;
    e = ChakraRTInterface::JsQueueBackgroundParse_Experimental(&scriptContents, &cookie);
    Assert(e == JsErrorCode::JsNoError);

    JsValueRef bgResult = nullptr;
    e = ChakraRTInterface::JsExecuteBackgroundParse_Experimental(
        cookie, scriptSource, WScriptJsrt::GetNextSourceContext(), scriptContents.fullPath,
        JsParseScriptAttributes::JsParseScriptAttributeNone,
        nullptr, //_In_ JsValueRef parserState,
        &bgResult);

    return e;
}

int32_t ExecuteTest(const std::string &fileName, JsRuntimeHandle &chRuntime, JsRuntimeAttributes &jsrtAttributes)
{
    int32_t hr = S_OK;
    const char *fileContents = nullptr;
    JsRuntimeHandle runtime = JS_INVALID_RUNTIME_HANDLE;
    uint32_t lengthBytes = 0;

    hr = Helpers::LoadScriptFromFile(fileName.c_str(), fileContents, &lengthBytes);

    IfFailGo(hr);
    if (HostConfigFlags::flags.GenerateLibraryByteCodeHeaderIsEnabled)
    {
        jsrtAttributes = static_cast<JsRuntimeAttributes>(jsrtAttributes | JsRuntimeAttributeSerializeLibraryByteCode);
    }

    IfJsErrorFailLog(ChakraRTInterface::JsCreateRuntime(jsrtAttributes, nullptr, &runtime));
    chRuntime = runtime;

    {
        JsContextRef context = JS_INVALID_REFERENCE;
        IfJsErrorFailLog(ChakraRTInterface::JsCreateContext(runtime, &context));
        IfJsErrorFailLog(ChakraRTInterface::JsSetCurrentContext(context));
    }

    if (!WScriptJsrt::Initialize())
    {
        IfFailGo(E_FAIL);
    }

    {
        auto fullPath = std::filesystem::path(fileName).lexically_normal();

        if (HostConfigFlags::flags.TrackRejectedPromises)
        {
            ChakraRTInterface::JsSetHostPromiseRejectionTracker(WScriptJsrt::PromiseRejectionTrackerCallback, nullptr);
        }

        if (HostConfigFlags::flags.GenerateLibraryByteCodeHeaderIsEnabled)
        {
            IfFailGo(CreateLibraryByteCode(fileContents));
        }
        else if (HostConfigFlags::flags.SerializedIsEnabled)
        {
            CreateAndRunSerializedScript(fileName.c_str(), fileContents, lengthBytes, WScriptJsrt::FinalizeFree,
                                         fullPath, chRuntime, jsrtAttributes);
        }
        else if (HostConfigFlags::flags.GenerateParserStateCacheIsEnabled)
        {
            CreateParserState(fileContents, WScriptJsrt::FinalizeFree);
        }
        else if (HostConfigFlags::flags.UseParserStateCacheIsEnabled)
        {
            CreateParserStateAndRunScript(fileName.c_str(), fileContents, lengthBytes, WScriptJsrt::FinalizeFree,
                                          fullPath, chRuntime, jsrtAttributes);
        }
        else
        {
            IfFailGo(RunScript(fileName.c_str(), fileContents, lengthBytes, WScriptJsrt::FinalizeFree, nullptr,
                               fullPath, nullptr));
        }
    }
Error:
    ChakraRTInterface::JsSetCurrentContext(nullptr);

    if (runtime != JS_INVALID_RUNTIME_HANDLE)
    {
        ChakraRTInterface::JsDisposeRuntime(runtime);
    }

    fflush(NULL);

    return hr;
}

int32_t ExecuteTestWithMemoryCheck(const std::string &fileName, JsRuntimeHandle &chRuntime,
                                   JsRuntimeAttributes &jsrtAttributes)
{
    int32_t hr = E_FAIL;
    // REVIEW: Do we need a SEH handler here?
    hr = ExecuteTest(fileName, chRuntime, jsrtAttributes);
    if (FAILED(hr))
        exit(0);

    fflush(NULL);
    return hr;
}

int main_internal(chakra_rs::config::CoreConfig config)
{
    JsRuntimeHandle chRuntime = JS_INVALID_RUNTIME_HANDLE;
    JsRuntimeAttributes jsrtAttributes = JsRuntimeAttributeNone;

    HostConfigFlags::pfnPrintUsage = chakra_rs::chhelper::print_usage;
    HostConfigFlags::vargsVal = chakra_rs::str_helper::split_args(config.args);

    std::vector<std::u16string> vargs;

    for (auto arg : config.args)
    {
        std::u16string s;
        NarrowStringToWideDynamic(arg.c_str(), s);
        vargs.push_back(std::move(s));
    }

    ChakraRTInterface::ArgInfo argInfo = {vargs, chakra_rs::chhelper::print_usage, {}};
    const bool success = ChakraRTInterface::LoadChakraDll(&argInfo);

    // handle command line flags
    OnChakraCoreLoaded();

    if (argInfo.filename_.empty())
    {
        argInfo.filename_ = static_cast<std::string>(config.args[1]);
    }

    int32_t exitCode = E_FAIL;
    if (success)
    {
        // On linux, execute on the same thread
        exitCode = ExecuteTestWithMemoryCheck(argInfo.filename_, chRuntime, jsrtAttributes);
    }

    PAL_Shutdown();
    int retval = exitCode;

#ifdef NO_SANITIZE_ADDRESS_CHECK
    pthread_exit(&retval);
#else
    return retval;
#endif
}
